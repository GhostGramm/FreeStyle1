export function lerpVec3(a: Vec3, b: Vec3, t: number): Vec3 {

    t = math.clamp01(t);
    return new Vec3(
        a.x + (b.x - a.x) * t,
        a.y + (b.y - a.y) * t,
        a.z + (b.z - a.z) * t
    );
}

